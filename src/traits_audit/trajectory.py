"""Trajectory records and cross-demo DMDc analysis for AL policy comparison.

This module standardises how the traits-audit demos (and any AL loop) emit the
per-step data that :mod:`traits_audit.dmdc` consumes, so that a single analysis
harness can compute the policy-comparison metrics uniformly across domains.

The core object is :class:`TrajectoryRecord`. Each AL loop appends one
:class:`StepObservation` per step and calls :meth:`TrajectoryRecord.finalize`
at the end; :func:`analyze_trajectory` then produces the three-fit DMDc
decomposition (state-only, uncertainty-only, joint), the spectral radii, the
controllability Gramian, and block-bootstrap confidence intervals.

Design decisions (see paper1_NMI_metric_outline.md §2, §3):
  * The **action** is the protocol / query point the policy chose at each step.
    It must be the real action, never a placeholder — otherwise ``B`` is
    unidentified and the controllability Gramian is meaningless. The camd demo
    currently passes ``np.ones((T, 1))``; ``from_camd`` fixes this.
  * The **uncertainty** may be scalar or vector. The A_state / A_unc split and
    the Gramian directional analysis are only informative when it is a vector;
    :func:`analyze_trajectory` reports ``uncertainty_dim`` so a scalar-only
    domain is flagged rather than silently degenerate.
  * The **warm-up mask** flags steps generated by a fallback policy (e.g. LCB
    before ``min_fit_obs``) or before detrending stabilises; these are excluded
    from the DMDc fit.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Sequence

import numpy as np

from . import dmdc as _dmdc


# ─────────────────────────────────────────────────────────────────────────────
# Per-step observation and the accumulated record
# ─────────────────────────────────────────────────────────────────────────────

@dataclass
class StepObservation:
    """One active-learning step.

    Parameters
    ----------
    state : np.ndarray, shape (n_state,)
        The physical / descriptor state after the query (e.g. scaled PCA coords,
        normalised (C-rate, T), ECM posterior means). Detrended downstream.
    uncertainty : np.ndarray, shape (n_unc,) or scalar
        Surrogate posterior standard deviation. A per-output vector is strongly
        preferred; a scalar is accepted but limits the A_unc / Gramian analysis.
    action : np.ndarray, shape (m,)
        The protocol / query point selected by the policy at this step. This is
        the DMDc control input and must be the genuine action.
    is_warmup : bool
        True if this step was produced by a fallback policy or lies in the
        detrending warm-up window; excluded from the DMDc fit.
    """

    state: np.ndarray
    uncertainty: np.ndarray
    action: np.ndarray
    is_warmup: bool = False

    def __post_init__(self) -> None:
        self.state = np.atleast_1d(np.asarray(self.state, dtype=np.float64))
        self.uncertainty = np.atleast_1d(np.asarray(self.uncertainty, dtype=np.float64))
        self.action = np.atleast_1d(np.asarray(self.action, dtype=np.float64))


@dataclass
class TrajectoryRecord:
    """Accumulates :class:`StepObservation` and assembles DMDc-ready arrays.

    Usage inside an AL loop::

        rec = TrajectoryRecord(domain="pybamm", policy="LCB")
        for step in range(n_iter):
            ...
            rec.add(state=xi_n, uncertainty=sigma_q, action=xi_n,
                    is_warmup=(step < min_fit_obs))
        rec.finalize()
        result = analyze_trajectory(rec)
    """

    domain: str = ""
    policy: str = ""
    steps: list[StepObservation] = field(default_factory=list)

    # populated by finalize()
    states: np.ndarray | None = None          # (T, n_state)
    uncertainties: np.ndarray | None = None    # (T, n_unc)
    actions: np.ndarray | None = None          # (T, m)
    warmup_mask: np.ndarray | None = None      # (T,) bool

    def add(self, state, uncertainty, action, is_warmup: bool = False) -> None:
        self.steps.append(StepObservation(state, uncertainty, action, is_warmup))

    def finalize(self) -> "TrajectoryRecord":
        if not self.steps:
            raise ValueError("TrajectoryRecord has no steps.")
        # Enforce consistent per-step dimensions.
        n_state = self.steps[0].state.shape[0]
        n_unc = self.steps[0].uncertainty.shape[0]
        m = self.steps[0].action.shape[0]
        for t, obs in enumerate(self.steps):
            if obs.state.shape[0] != n_state:
                raise ValueError(f"step {t}: state dim {obs.state.shape[0]} != {n_state}")
            if obs.uncertainty.shape[0] != n_unc:
                raise ValueError(f"step {t}: uncertainty dim {obs.uncertainty.shape[0]} != {n_unc}")
            if obs.action.shape[0] != m:
                raise ValueError(f"step {t}: action dim {obs.action.shape[0]} != {m}")
        self.states = np.vstack([o.state for o in self.steps])
        self.uncertainties = np.vstack([o.uncertainty for o in self.steps])
        self.actions = np.vstack([o.action for o in self.steps])
        self.warmup_mask = np.array([o.is_warmup for o in self.steps], dtype=bool)
        return self


# ─────────────────────────────────────────────────────────────────────────────
# Detrending (per-protocol-group causal EMA)
# ─────────────────────────────────────────────────────────────────────────────

def detrend_states(
    states: np.ndarray,
    actions: np.ndarray,
    alpha: float = 0.1,
    n_groups: int = 8,
    warmup: int = 20,
    seed: int = 0,
) -> np.ndarray:
    """Remove monotone drift with a per-protocol-group causal EMA.

    Battery/materials state trajectories drift monotonically; a global DMDc on
    the raw state absorbs the drift into ``A`` and produces spurious
    ``|lambda| > 1``. This subtracts a causal (past-only) EMA reference computed
    within k-means protocol groups fit on the first ``warmup`` actions.

    Returns the detrended state array, same shape as ``states``.
    """
    from sklearn.cluster import KMeans

    T = len(states)
    n_groups = int(min(n_groups, max(1, T // 4)))
    warmup = int(min(warmup, T))
    km = KMeans(n_clusters=n_groups, n_init=10, random_state=seed)
    km.fit(actions[:warmup] if warmup >= n_groups else actions)
    labels = km.predict(actions)

    detrended = np.empty_like(states, dtype=np.float64)
    ema = {g: None for g in range(n_groups)}
    for t in range(T):
        g = int(labels[t])
        prev = ema[g]
        if prev is None:
            ref = states[t]           # first sight of this group: reference = itself
        else:
            ref = prev                # causal: use only the past EMA
        detrended[t] = states[t] - ref
        ema[g] = states[t] if prev is None else (alpha * states[t] + (1 - alpha) * prev)
    return detrended


# ─────────────────────────────────────────────────────────────────────────────
# The three-fit analysis
# ─────────────────────────────────────────────────────────────────────────────

@dataclass
class TrajectoryDMDcResult:
    domain: str
    policy: str
    rho_A_state: float
    rho_A_unc: float
    rho_A_joint: float
    tr_Wc: float
    cond_Wc: float
    Wc_singular_values: np.ndarray
    uncertainty_dim: int
    n_fit: int
    rho_joint_ci: tuple[float, float] | None = None
    warmup_excluded: int = 0
    scalar_uncertainty_warning: bool = False


def _rho(A: np.ndarray) -> float:
    if A.size == 0:
        return float("nan")
    return float(np.max(np.abs(np.linalg.eigvals(A))))


def analyze_trajectory(
    rec: TrajectoryRecord,
    n_components: int = 8,
    detrend: bool = True,
    block_bootstrap: bool = True,
    n_boot: int = 200,
    block_len: int = 8,
    seed: int = 42,
) -> TrajectoryDMDcResult:
    """Run the three-fit DMDc decomposition on a finalised trajectory.

    Fits ``A_state`` (state subvector — sanity check, should be policy-invariant),
    ``A_unc`` (uncertainty subvector — the policy-discriminating signal), and the
    joint ``A`` (primary metric). Computes the controllability Gramian and, if
    requested, a block-bootstrap CI on the joint spectral radius.
    """
    if rec.states is None:
        rec.finalize()

    keep = ~rec.warmup_mask
    states = rec.states[keep]
    unc = rec.uncertainties[keep]
    actions = rec.actions[keep]
    warmup_excluded = int(rec.warmup_mask.sum())

    if detrend:
        states = detrend_states(states, actions, seed=seed)

    unc_dim = unc.shape[1]
    scalar_warn = unc_dim == 1

    aug = np.hstack([states, unc])                       # (T, n_state + n_unc)

    # Joint fit (primary metric) + Gramian
    A_joint, B_joint, U_joint = _dmdc.fit_dmdc(aug, actions, n_components=n_components)
    Wc = _dmdc.compute_gramians(A_joint, B_joint)
    Wc_c = Wc[0] if isinstance(Wc, tuple) else Wc      # compute_gramians may return (Wc, Wo)
    sv = np.linalg.svd(Wc_c, compute_uv=False)
    tr_Wc = float(np.trace(Wc_c))
    cond_Wc = float(sv[0] / sv[-1]) if sv[-1] > 0 else float("inf")

    # State-only and uncertainty-only fits
    A_state, _, _ = _dmdc.fit_dmdc(states, actions, n_components=n_components)
    A_unc, _, _ = _dmdc.fit_dmdc(unc, actions, n_components=min(n_components, unc_dim))

    # Block-bootstrap CI on joint rho
    rho_ci = None
    if block_bootstrap:
        rho_ci = _block_bootstrap_rho(aug, actions, n_components, n_boot, block_len, seed)

    return TrajectoryDMDcResult(
        domain=rec.domain,
        policy=rec.policy,
        rho_A_state=_rho(A_state),
        rho_A_unc=_rho(A_unc),
        rho_A_joint=_rho(A_joint),
        tr_Wc=tr_Wc,
        cond_Wc=cond_Wc,
        Wc_singular_values=sv,
        uncertainty_dim=unc_dim,
        n_fit=len(aug) - 1,
        rho_joint_ci=rho_ci,
        warmup_excluded=warmup_excluded,
        scalar_uncertainty_warning=scalar_warn,
    )


def _block_bootstrap_rho(aug, actions, n_components, n_boot, block_len, seed):
    """Moving-block bootstrap CI for rho(A_joint) — respects autocorrelation.

    The i.i.d. ``bootstrap_eig_ci`` in dmdc.py resamples individual transition
    pairs, which is invalid for an autocorrelated AL trajectory. This resamples
    contiguous blocks of length ``block_len`` to preserve short-range temporal
    structure.
    """
    rng = np.random.default_rng(seed)
    T = len(aug)
    n_blocks = max(1, (T - 1) // block_len)
    rhos = []
    for _ in range(n_boot):
        starts = rng.integers(0, T - block_len, size=n_blocks)
        idx = np.concatenate([np.arange(s, s + block_len) for s in starts])
        idx = idx[idx < T]
        if len(idx) < n_components + actions.shape[1] + 2:
            continue
        try:
            A_b, _, _ = _dmdc.fit_dmdc(aug[idx], actions[idx], n_components=n_components)
            rhos.append(_rho(A_b))
        except np.linalg.LinAlgError:
            continue
    if not rhos:
        return None
    lo, hi = np.percentile(rhos, [2.5, 97.5])
    return (float(lo), float(hi))


# ─────────────────────────────────────────────────────────────────────────────
# Demo adapters — collect a TrajectoryRecord from each demo's native loop data
# ─────────────────────────────────────────────────────────────────────────────

def from_stepwise(
    states: Sequence[np.ndarray],
    uncertainties: Sequence[np.ndarray],
    actions: Sequence[np.ndarray],
    warmup_mask: Sequence[bool] | None = None,
    domain: str = "",
    policy: str = "",
) -> TrajectoryRecord:
    """Generic builder from three equal-length per-step sequences."""
    T = len(states)
    if not (len(uncertainties) == len(actions) == T):
        raise ValueError("states, uncertainties, actions must be equal length")
    if warmup_mask is None:
        warmup_mask = [False] * T
    rec = TrajectoryRecord(domain=domain, policy=policy)
    for t in range(T):
        rec.add(states[t], uncertainties[t], actions[t], bool(warmup_mask[t]))
    return rec.finalize()


def from_pybamm(result_lists, policy: str = "UCB", warmup: int = 0) -> TrajectoryRecord:
    """Adapt the ta-pybamm loop.

    ta-pybamm records ``uncertainties`` (list of ``sigma_q``) and ``queried_n``
    (normalised query coords). The query point IS both the state and the action
    here (2-D: normalised C-rate, temperature). Uncertainty is scalar — the
    A_unc analysis will be 1-D (flagged).

    Parameters
    ----------
    result_lists : dict with keys 'uncertainties', 'queried_n'
    """
    unc = result_lists["uncertainties"]
    q = result_lists["queried_n"]
    warmup_mask = [i < warmup for i in range(len(q))]
    return from_stepwise(q, unc, q, warmup_mask, domain="pybamm", policy=policy)


def from_sdl(result_lists, policy: str = "GP-UCB", warmup: int = 0) -> TrajectoryRecord:
    """Adapt the ta-sdl loop.

    ta-sdl records ``uncertainties`` (``sigma_safe``) and ``queried_norm``
    (normalised query state, 3-D). Query point is state and action; scalar
    uncertainty.
    """
    unc = result_lists["uncertainties"]
    q = result_lists["queried_norm"]
    warmup_mask = [i < warmup for i in range(len(q))]
    return from_stepwise(q, unc, q, warmup_mask, domain="sdl", policy=policy)


def from_camd(result_lists, policy: str = "QBC", warmup: int = 0) -> TrajectoryRecord:
    """Adapt the ta-camd loop — WITH the action-wiring fix.

    The current camd demo builds ``aug_states_list`` = ``[scaled PCA coords |
    mean_std]`` and calls ``stability_convergence`` with a DUMMY action
    ``np.ones((T, 1))``. That makes ``B`` unidentified and the Gramian
    meaningless. This adapter separates state from uncertainty and passes the
    genuine queried point as the action.

    Requires the camd loop to additionally record ``queried_batches`` (the
    descriptor coords of each queried candidate) — already accumulated in the
    demo; expose the per-step queried point as ``queried_coords``.

    Parameters
    ----------
    result_lists : dict with keys
        'pca_coords'      : list of scaled PCA coords per step (the state)
        'uncertainties'   : list of per-output std vectors (preferred) or scalars
        'queried_coords'  : list of the queried descriptor point (the action)
    """
    states = result_lists["pca_coords"]
    unc = result_lists["uncertainties"]
    actions = result_lists["queried_coords"]        # the fix: real action, not ones()
    warmup_mask = [i < warmup for i in range(len(states))]
    return from_stepwise(states, unc, actions, warmup_mask, domain="camd", policy=policy)
